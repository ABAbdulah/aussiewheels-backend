import type { Listing } from "@prisma/client";
import { z } from "zod";

// ── API DTO (wire shape returned to clients) ───────────────────────────────
// Enums are lowercased and a few convenience fields (title, location,
// photoCount) are derived so the frontend can render cards without extra work.

export const listingDtoSchema = z.object({
  id: z.string(),
  slug: z.string(),
  vertical: z.string(),
  make: z.string(),
  model: z.string(),
  variant: z.string().nullable(),
  year: z.number().int(),
  title: z.string(),
  price: z.number().int(),
  driveawayPrice: z.number().int().nullable(),
  priceIndicator: z.enum(["great", "good", "fair", "high"]).nullable(),
  odometer: z.number().int(),
  transmission: z.string(),
  fuel: z.string(),
  bodyType: z.string(),
  driveType: z.string().nullable(),
  doors: z.number().int().nullable(),
  seats: z.number().int().nullable(),
  colour: z.string().nullable(),
  engine: z.string().nullable(),
  cylinders: z.number().int().nullable(),
  powerKw: z.number().int().nullable(),
  condition: z.enum(["new", "used", "demo"]),
  sellerType: z.enum(["dealer", "private"]),
  sellerName: z.string().nullable(),
  suburb: z.string(),
  state: z.string(),
  location: z.string(),
  description: z.string(),
  features: z.array(z.string()),
  images: z.array(z.string()),
  photoCount: z.number().int(),
  vin: z.string().nullable(),
  rego: z.string().nullable(),
  warrantyMonths: z.number().int().nullable(),
  warrantyKm: z.number().int().nullable(),
  evRangeKm: z.number().int().nullable(),
  specialOffer: z.boolean(),
  tradeInEligible: z.boolean(),
  reviewScore: z.number().int().nullable(),
  createdAt: z.string(),
});

export type ListingDto = z.infer<typeof listingDtoSchema>;

export const listResponseSchema = z.object({
  items: z.array(listingDtoSchema),
  total: z.number().int(),
  page: z.number().int(),
  pageSize: z.number().int(),
  totalPages: z.number().int(),
});

const PRICE_INDICATOR = { GREAT: "great", GOOD: "good", FAIR: "fair", HIGH: "high" } as const;

export function toDto(l: Listing): ListingDto {
  return {
    id: l.id,
    slug: l.slug,
    vertical: l.vertical,
    make: l.make,
    model: l.model,
    variant: l.variant,
    year: l.year,
    title: `${l.year} ${l.make} ${l.model}${l.variant ? ` ${l.variant}` : ""}`,
    price: l.price,
    driveawayPrice: l.driveawayPrice,
    priceIndicator: l.priceIndicator ? PRICE_INDICATOR[l.priceIndicator] : null,
    odometer: l.odometer,
    transmission: l.transmission,
    fuel: l.fuel,
    bodyType: l.bodyType,
    driveType: l.driveType,
    doors: l.doors,
    seats: l.seats,
    colour: l.colour,
    engine: l.engine,
    cylinders: l.cylinders,
    powerKw: l.powerKw,
    condition: l.condition.toLowerCase() as ListingDto["condition"],
    sellerType: l.sellerType.toLowerCase() as ListingDto["sellerType"],
    sellerName: l.sellerName,
    suburb: l.suburb,
    state: l.state,
    location: `${l.suburb}, ${l.state}`,
    description: l.description,
    features: l.features,
    images: l.images,
    photoCount: l.images.length,
    vin: l.vin,
    rego: l.rego,
    warrantyMonths: l.warrantyMonths,
    warrantyKm: l.warrantyKm,
    evRangeKm: l.evRangeKm,
    specialOffer: l.specialOffer,
    tradeInEligible: l.tradeInEligible,
    reviewScore: l.reviewScore,
    createdAt: l.createdAt.toISOString(),
  };
}
